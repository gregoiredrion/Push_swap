#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t len)
{
	size_t	c;
	char	*ret;

	ret = s1;
	c = 0;
	while (*ret)
		ret++;
	while (c++ < len && *s2)
		*ret++ = *s2++;
	*ret = '\0';
	return (s1);
}
